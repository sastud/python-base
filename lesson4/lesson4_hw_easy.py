# Задание-3:
# Дан список, заполненный произвольными числами.
# Получить список из элементов исходного, удовлетворяющих следующим условиям:
# + Элемент кратен 3
# + Элемент положительный
# + Элемент не кратен 4

print('\n')
print('Задача-3\n')

list6 = [random.randint(-100, 100) for _ in range(10)]
list7 = [el for el in list6 if el % 3 == 0 if el > 0 if el % 4 != 0]
print(list6)
print(list7)
