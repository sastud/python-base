# Задание-2:
# Даны два списка фруктов.
# Получить список фруктов, присутствующих в обоих исходных списках.

print('\n')
print('Задача-2\n')

list3 = ['Арбуз', 'Абрикос', 'Дыня', 'Яблоко', 'Апельсин', 'Мандарин', 'Ананас', 'Манго']
list4 = ['Лимон', 'Абрикос', 'Ирга', 'Яблоко', 'Джамбоза', 'Мандарин', 'Помело', 'Манго']
list5 = [el for el in list3 if el in list4]
print(list3)
print(list4)
print(list5)

